<?php
namespace App\Models;

use Illuminate\Foundation\Auth\User as Authenticatable;
use Laravel\Sanctum\HasApiTokens;  // ถ้าใช้ Sanctum
use Illuminate\Notifications\Notifiable;

class User extends Authenticatable
{
    use HasApiTokens, Notifiable;

    // ถ้าตารางชื่อ user ไม่ใช่ users
    protected $table = 'user';

    // ถ้า primary key คือ User_Code
    protected $primaryKey = 'User_Code';
    public $incrementing = false;     // ถ้าไม่ได้ auto-increment
    protected $keyType = 'int';

    protected $fillable = [
        'User_Code',
        'User_Name',
        'email',
        'tel',
        'password',
        'User_Type',
        'Village_Code',
        'status',
        'home_Code',
    ];

    protected $hidden = [
        'password',
    ];
}
